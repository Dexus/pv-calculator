class _CouplingsEditor extends StatelessWidget {
  const _CouplingsEditor({
    required this.topo,
    required this.draft,
    required this.inverterIds,
    required this.onChanged,
  });

  final TopologyGraphDraft topo;
  final ConfigDraft draft;
  final List<String> inverterIds;
  final VoidCallback onChanged;

  BatteryCouplingDraft _ensureCouplingFor(String batteryId) {
    for (final c in topo.couplings) {
      if (c.batteryId == batteryId) return c;
    }
    final c = BatteryCouplingDraft(batteryId: batteryId);
    topo.couplings.add(c);
    return c;
  }

  @override
  Widget build(BuildContext context) {
    final l = AppLocalizations.of(context);
    final dcBusIds = [for (final b in topo.dcBuses) b.id];
    return Column(crossAxisAlignment: CrossAxisAlignment.start, children: [
      Text(l.topologyCouplingsTitle, style: Theme.of(context).textTheme.titleSmall),
      if (draft.batteries.isEmpty)
        Text(l.topologyCouplingsEmpty, style: Theme.of(context).textTheme.bodySmall)
      else
        for (final battery in draft.batteries) ...[
          const SizedBox(height: 4),
          Builder(builder: (context) {
            final coupling = _ensureCouplingFor(battery.id);
            return Wrap(spacing: 8, runSpacing: 8, crossAxisAlignment: WrapCrossAlignment.center, children: [
              SizedBox(width: 180, child: Text(battery.id, style: Theme.of(context).textTheme.bodyMedium)),
              SegmentedButton<bool>(
                segments: [
                  ButtonSegment(value: true, label: Text(l.topologyCouplingAc)),
                  ButtonSegment(value: false, label: Text(l.topologyCouplingDc)),
                ],
                selected: {coupling.acCoupled},
                onSelectionChanged: (v) {
                  coupling.acCoupled = v.first;
                  // Drop the now-hidden picker's selection so a switch
                  // back to AC doesn't silently apply a stale dcBusId,
                  // and a DC-coupled battery doesn't keep a stale
                  // inverterId that would otherwise feed the engine's
                  // AC cap path.
                  if (coupling.acCoupled) {
                    coupling.dcBusId = null;
                  } else {
                    coupling.inverterId = null;
                  }
                  onChanged();
                },
              ),
              if (!coupling.acCoupled)
                SizedBox(
                  width: 220,
                  child: DropdownButtonFormField<String>(
                    key: Key('topology-coupling-${battery.id}-dcbus'),
                    isExpanded: true,
                    initialValue: dcBusIds.contains(coupling.dcBusId) ? coupling.dcBusId : null,
                    decoration: InputDecoration(labelText: l.topologyCouplingDcBus, isDense: true),
                    items: [
                      for (final id in dcBusIds) DropdownMenuItem(value: id, child: Text(id)),
                    ],
                    onChanged: (v) {
                      coupling.dcBusId = v;
                      onChanged();
                    },
                  ),
                ),
              if (coupling.acCoupled)
                SizedBox(
                  width: 240,
                  child: DropdownButtonFormField<String?>(
                    key: Key('topology-coupling-${battery.id}-inverter'),
                    isExpanded: true,
                    initialValue: inverterIds.contains(coupling.inverterId) ? coupling.inverterId : null,
                    decoration: InputDecoration(
                      labelText: l.topologyCouplingInverter,
                      isDense: true,
                      helperText: l.topologyCouplingInverterHelp,
                    ),
                    items: [
                      DropdownMenuItem<String?>(value: null, child: Text(l.topologyCouplingInverterNone)),
                      for (final id in inverterIds) DropdownMenuItem<String?>(value: id, child: Text(id)),
                    ],
                    onChanged: (v) {
                      coupling.inverterId = v;
                      onChanged();
                    },
                  ),
                ),
            ]);
          }),
        ],
    ]);
  }
}
