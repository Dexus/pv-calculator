/// Phase 4: optional explicit topology graph. While the master switch
/// is off, the engine derives a default topology from the flat
/// arrays/inverters/batteries lists and the section stays a thin info
/// chip. Toggling on seeds the draft from the legacy adapter and reveals
/// editors for DC/AC buses, MPPTs (read-only), edges and battery
/// couplings (PRD FR-08; Architektur §4).
class TopologySection extends StatelessWidget {
  const TopologySection({super.key});

  @override
  Widget build(BuildContext context) {
    final controller = context.watch<ProjectController>();
    final draft = controller.draft;
    final l = AppLocalizations.of(context);
    final topo = draft.topology;
    final inverterIds = [for (final i in draft.inverters) i.id];

    return Card(
      child: ExpansionTile(
        title: Text(l.topologyTitle),
        leading: const Icon(Icons.account_tree_outlined),
        initiallyExpanded: topo.enabled,
        children: [
          Padding(
            padding: const EdgeInsets.fromLTRB(16, 0, 16, 16),
            child: Column(crossAxisAlignment: CrossAxisAlignment.start, children: [
              SwitchListTile(
                key: const Key('topology-enable'),
                contentPadding: EdgeInsets.zero,
                title: Text(l.topologyEnable),
                value: topo.enabled,
                onChanged: (v) {
                  topo.enabled = v;
                  if (v && topo.dcBuses.isEmpty && topo.acBuses.isEmpty) {
                    topo.seedFromConfig(draft);
                  }
                  controller.touch();
                },
              ),
              if (!topo.enabled)
                Row(children: [
                  const Icon(Icons.info_outline, size: 16),
                  const SizedBox(width: 6),
                  Expanded(child: Text(
                    l.topologyAutoGeneratedInfo,
                    style: Theme.of(context).textTheme.bodySmall,
                  )),
                ])
              else ...[
                Align(
                  alignment: Alignment.centerLeft,
                  child: TextButton.icon(
                    key: const Key('topology-seed-from-legacy'),
                    onPressed: () {
                      topo.seedFromConfig(draft);
                      controller.touch();
                    },
                    icon: const Icon(Icons.auto_fix_high),
                    label: Text(l.topologySeedFromLegacy),
                  ),
                ),
                const SizedBox(height: 8),
                _DcBusesEditor(topo: topo, onChanged: controller.touch),
                const SizedBox(height: 12),
                _AcBusesEditor(topo: topo, onChanged: controller.touch),
                const SizedBox(height: 12),
                _MpptList(topo: topo),
                const SizedBox(height: 12),
                _EdgesEditor(topo: topo, onChanged: controller.touch),
                const SizedBox(height: 12),
                _CouplingsEditor(
                  topo: topo,
                  draft: draft,
                  inverterIds: inverterIds,
                  onChanged: controller.touch,
                ),
              ],
            ]),
          ),
        ],
      ),
    );
  }
}

class _DcBusesEditor extends StatelessWidget {
  const _DcBusesEditor({required this.topo, required this.onChanged});
  final TopologyGraphDraft topo;
  final VoidCallback onChanged;

  @override
  Widget build(BuildContext context) {
    final l = AppLocalizations.of(context);
    return Column(crossAxisAlignment: CrossAxisAlignment.start, children: [
      Row(children: [
        Expanded(child: Text(l.topologyDcBusesTitle, style: Theme.of(context).textTheme.titleSmall)),
        TextButton.icon(
          key: const Key('topology-add-dc-bus'),
          onPressed: () {
            final n = topo.dcBuses.length + 1;
            topo.dcBuses.add(DcBusDraft(id: 'dc-bus-$n'));
            onChanged();
          },
          icon: const Icon(Icons.add),
          label: Text(l.topologyAddDcBus),
        ),
      ]),
      for (var i = 0; i < topo.dcBuses.length; i++)
        Padding(
          padding: const EdgeInsets.symmetric(vertical: 4),
          child: Row(children: [
            SizedBox(width: 180, child: StringField(
              key: ValueKey('topology-dc-${topo.dcBuses[i].id}-id'),
              label: l.fieldId, initialValue: topo.dcBuses[i].id, required: true,
              onChanged: (v) { topo.dcBuses[i].id = v; onChanged(); },
            )),
            const SizedBox(width: 8),
            SizedBox(width: 220, child: StringField(
              label: l.fieldLabel, initialValue: topo.dcBuses[i].label,
              onChanged: (v) { topo.dcBuses[i].label = v; onChanged(); },
            )),
            IconButton(
              tooltip: l.commonRemove,
              onPressed: () { topo.dcBuses.removeAt(i); onChanged(); },
              icon: const Icon(Icons.close),
            ),
          ]),
        ),
    ]);
  }
}

